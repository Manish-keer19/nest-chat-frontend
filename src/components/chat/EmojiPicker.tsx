export const EmojiPicker: React.FC<EmojiPickerProps> = ({ onEmojiSelect, onClose }) => {
    const [activeCategory, setActiveCategory] = useState<string>('Smileys');
    const [searchQuery, setSearchQuery] = useState('');

    const categories = Object.keys(emojiCategories);

    // Memoize filtered emojis for performance
    const filteredEmojis = useMemo(() => {
        return searchQuery
            ? Object.values(emojiCategories).flat().filter(emoji => emoji.includes(searchQuery))
            : emojiCategories[activeCategory as keyof typeof emojiCategories];
    }, [searchQuery, activeCategory]);

    return (
        <motion.div
            initial={{ opacity: 0, scale: 0.95, y: 10 }}
            animate={{ opacity: 1, scale: 1, y: 0 }}
            exit={{ opacity: 0, scale: 0.95, y: 10 }}
            transition={{ duration: 0.2 }}
            className="absolute bottom-full right-0 mb-2 w-[320px] md:w-[380px] bg-gradient-to-br from-[#1e293b] to-[#0f172a] border border-white/10 rounded-2xl shadow-2xl overflow-hidden backdrop-blur-xl z-50"
            onClick={(e) => e.stopPropagation()}
        >
            {/* Header */}
            <div className="p-3 border-b border-white/10 bg-[#1e293b]/50 backdrop-blur-sm">
                <div className="flex items-center justify-between mb-2">
                    <h3 className="text-sm font-bold text-white">Premium Emojis</h3>
                    <button
                        onClick={onClose}
                        className="p-1.5 text-slate-400 hover:text-white hover:bg-white/10 rounded-lg transition-all"
                    >
                        <X size={16} />
                    </button>
                </div>

                {/* Search */}
                <div className="relative">
                    <Search className="absolute left-2.5 top-1/2 -translate-y-1/2 w-3.5 h-3.5 text-slate-500" />
                    <input
                        type="text"
                        placeholder="Search emojis..."
                        value={searchQuery}
                        onChange={(e) => setSearchQuery(e.target.value)}
                        className="w-full bg-[#0f172a]/50 border border-white/10 rounded-lg pl-8 pr-3 py-1.5 text-xs text-white placeholder-slate-500 focus:outline-none focus:border-blue-500/50 transition-all"
                    />
                </div>
            </div>

            {/* Categories */}
            {!searchQuery && (
                <div className="flex gap-1 p-2 border-b border-white/10 bg-[#0f172a]/30 overflow-x-auto scrollbar-thin scrollbar-thumb-slate-600">
                    {categories.map((category) => (
                        <button
                            key={category}
                            onClick={() => setActiveCategory(category)}
                            className={`px-3 py-1.5 rounded-lg text-xs font-medium whitespace-nowrap transition-all ${activeCategory === category
                                ? 'bg-gradient-to-r from-blue-600 to-purple-600 text-white shadow-lg shadow-blue-500/25'
                                : 'text-slate-400 hover:text-white hover:bg-white/5'
                                }`}
                        >
                            {category}
                        </button>
                    ))}
                </div>
            )}

            {/* Emoji Grid */}
            <div className="p-3 max-h-[280px] overflow-y-auto scrollbar-thin scrollbar-thumb-slate-600 scrollbar-track-transparent">
                <div className="grid grid-cols-8 gap-1">
                    {filteredEmojis.map((emoji, index) => (
                        <motion.button
                            key={`${emoji}-${index}`}
                            initial={{ opacity: 0, scale: 0.8 }}
                            animate={{ opacity: 1, scale: 1 }}
                            transition={{ delay: index * 0.01 }}
                            onClick={() => {
                                onEmojiSelect(emoji);
                                onClose();
                            }}
                            className="w-10 h-10 flex items-center justify-center text-2xl hover:bg-white/10 rounded-lg transition-all hover:scale-110 active:scale-95"
                            title={emoji}
                        >
                            {emoji}
                        </motion.button>
                    ))}
                </div>

                {filteredEmojis.length === 0 && (
                    <div className="text-center py-8 text-slate-500 text-sm">
                        No emojis found
                    </div>
                )}
            </div>

            {/* Footer */}
            <div className="p-2 border-t border-white/10 bg-[#0f172a]/30 text-center">
                <p className="text-[10px] text-slate-500">
                    Click any emoji to insert • {filteredEmojis.length} emojis available
                </p>
            </div>
        </motion.div>
    );
};
